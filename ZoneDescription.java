public class ZoneDescription {
    private int xBase;
    private int yBase;
    private int xBound;
    private int yBound;
    private int matrixSize;
    
    public int getxBase() {
        return xBase;
    }
    public int getyBase() {
        return yBase;
    }
    public int getxBound() {
        return xBound;
    }
    public int getyBound() {
        return yBound;
    }
    public int getMatrixSize() {
        return matrixSize;
    }
}
